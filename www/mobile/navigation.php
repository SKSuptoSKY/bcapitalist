<script type="text/javascript">
$(document).ready(function(){
	$('.btn_navi').click(function(){
		$('.btn_navi').stop(true,true).toggleClass('on');
		$('.navi').stop(true,true).fadeToggle();			
	});
	$('.navi h3').click(function(){
		$(this).stop(true,true).toggleClass('on');		
		var navi_h3 = $(this).next('.sub_navi').stop(true).slideToggle();
		$('.sub_navi').not(navi_h3).stop(true).slideUp();
		$('.sub_navi').not(navi_h3).prev('h3').removeClass('on');
		return false;
	});
	$('.sub_navi li a	').click(function(){
		$('.btn_navi').stop(true,true).removeClass('on');
		$('.navi').hide();			
	});

	$('.navi_bg').click(function(){
		$('.btn_navi').stop(true,true).removeClass('on');
		$('.navi').hide();			
	});
});
</script>


